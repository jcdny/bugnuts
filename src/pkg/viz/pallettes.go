func sfc(color image.NRGBAColor, alpha float64) {
	if alpha == 0 {
		alpha = float64(color.A) / 255.0
	}
	fmt.Fprintf(os.Stdout, "v sfc %d %d %d %.2f\n",
		color.R, color.G, color.B, alpha)
}
func slc(color image.NRGBAColor, alpha float64) {
	if alpha == 0 {
		alpha = float64(color.A) / 255.0
	}
	fmt.Fprintf(os.Stdout, "v slc %d %d %d %.2f\n",
		color.R, color.G, color.B, alpha)
}

