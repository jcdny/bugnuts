type Path struct {
	steps []Location

	// A path can take reference another path
	refpath *Path
	offset  int
}

func (m *Map) PointsToPath(points []Point) *Path {
	path := Path{
		steps: make([]Location, len(points), len(points)),
	}

	for i, p := range points {
		// check pts describe a real path
		path.steps[i] = m.ToLocation(p)
	}

	return &path
}

